package org.geysermc.hydraulic.pack.bedrock.resource.render_controllers.rendercontrollers;

import java.lang.Float;

/**
 * On Fire Color
 * <p>
 * The color that will be overlayed when the object is on fire.
 */
public class OnFireColor {
  public Float r;

  public Float g;

  public Float b;

  public Float a;

  /**
   * A color definition as number, between 0 and 1.
   *
   * @return Color Number
   */
  public Float getR() {
    return this.r;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @param r Color Number
   */
  public void setR(float r) {
    this.r = r;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @return Color Number
   */
  public Float getG() {
    return this.g;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @param g Color Number
   */
  public void setG(float g) {
    this.g = g;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @return Color Number
   */
  public Float getB() {
    return this.b;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @param b Color Number
   */
  public void setB(float b) {
    this.b = b;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @return Color Number
   */
  public Float getA() {
    return this.a;
  }

  /**
   * A color definition as number, between 0 and 1.
   *
   * @param a Color Number
   */
  public void setA(float a) {
    this.a = a;
  }
}
