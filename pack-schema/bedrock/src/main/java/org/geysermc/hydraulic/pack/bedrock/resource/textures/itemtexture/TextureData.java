import java.lang.String;

/**
 * Texture Data
 */
public class TextureData {
  public String textures;

  /**
   * A texture file.
   *
   * @return Texture
   */
  public String getTextures() {
    return this.textures;
  }

  /**
   * A texture file.
   *
   * @param textures Texture
   */
  public void setTextures(String textures) {
    this.textures = textures;
  }
}
